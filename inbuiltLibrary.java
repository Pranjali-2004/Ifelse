public class inbuiltLibrary {
    public static void main(String[]args){
        //Standard Inbuilt Library

        //square root
        System.out.println(Math.sqrt(5));

      //  integer value by rounding down a float or double to the nearest integer
        System.out.println(Math.floor(5.3));

       // integer value by rounding up a float or double to the nearest integer
        System.out.println(Math.ceil(5.3));
    }
}
