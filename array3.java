public class array3 {
    public static void main(String[]args){
        //Program to run 2D array
        int[][]age= new int[3][4];
        age[0][1]= 34;
        age[0][2]=21;
        age[1][1]=32;
        age[1][2]= 21;
        System.out.println(age[0][1]);
        System.out.println(age[0][2]);
        System.out.println(age[1][1]);
        System.out.println(age[1][2]);
    }

}
